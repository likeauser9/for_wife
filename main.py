import turtle

window = turtle.Screen()
window.title("Hello")
window.bgcolor("black")

pen = turtle.Turtle()
pen.speed(1)
pen.width(3)
pen.color("white")

def draw_p():
    pen.penup()
    pen.goto(-450, 350)
    pen.pendown()
    pen.goto(-350, 250)
    pen.penup()
    pen.goto(-400, 350)
    pen.pendown()
    pen.goto(-400, 250)
    pen.penup()
    pen.goto(-350, 350)
    pen.pendown()
    pen.goto(-450, 250)

